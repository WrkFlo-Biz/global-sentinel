def event_to_feature_vector(event: Dict[str, Any], target_dim: int = 16) -> List[float]:
    """Convert an event into a fixed-length normalized feature vector."""
    features = [
        float(event.get("severity", 0.5)),
        min(float(event.get("vix_peak", 20.0)) / 100.0, 1.5),
        (float(event.get("oil_move_pct", 0.0)) + 100.0) / 200.0,
        (float(event.get("gold_move_pct", 0.0)) + 50.0) / 100.0,
        abs(float(event.get("sp500_drawdown_pct", 0.0))) / 60.0,
        min(float(event.get("duration_days") or 30) / 365.0, 1.0),
        1.0 if event.get("category") == "war" else 0.0,
        1.0 if event.get("category") == "pandemic" else 0.0,
        1.0 if event.get("category") == "financial_crisis" else 0.0,
        1.0 if event.get("category") == "supply_chain" else 0.0,
        1.0 if event.get("category") == "policy" else 0.0,
        1.0 if event.get("category") == "cyber" else 0.0,
        1.0 if event.get("category") == "climate" else 0.0,
        1.0 if event.get("category") == "terrorism" else 0.0,
        1.0 if event.get("usd_move") == "strengthen" else 0.0,
        1.0 if "vol" in str(event.get("intraday_pattern", "")).lower() else 0.0,
    ]
    if target_dim <= len(features):
        return features[:target_dim]
    return features + [0.0] * (target_dim - len(features))


def event_to_analog_entry(event: Dict[str, Any]) -> Dict[str, Any]:
    """Map an event into the existing HistoricalAnalogEngine library format."""
    category = event.get("category", "unknown")
    tags = sorted({category, *event.get("regime_signature", {}).keys()})
    return {
        "label": event.get("name", event.get("id", "unknown")),
        "tags": tags,
        "regime_markers": event.get("regime_signature", {}),
        "asset_impacts": {
            "winners": event.get("winners", []),
            "losers": event.get("losers", []),
            "intraday_pattern": event.get("intraday_pattern"),
            "optimal_day_trade": event.get("optimal_day_trade"),
            "recovery_shape": event.get("recovery_shape"),
        },
        "source_event_id": event.get("id"),
        "category": category,
        "severity": event.get("severity"),
    }


def build_analog_library(events: Iterable[Dict[str, Any]] | None = None) -> List[Dict[str, Any]]:
    """Build an analog-library payload from the crisis dataset."""
    return [event_to_analog_entry(event) for event in (events or CRISIS_EVENTS)]


def dataset_summary(events: Iterable[Dict[str, Any]] | None = None) -> Dict[str, Any]:
    """Return counts and high-level stats for reporting/tests."""
    event_list = list(events or CRISIS_EVENTS)
    categories = Counter(event["category"] for event in event_list)
    return {
        "event_count": len(event_list),
        "category_counts": dict(sorted(categories.items())),
        "playbook_count": len(CRISIS_PLAYBOOKS),
        "categories_with_playbooks": sorted(CRISIS_PLAYBOOKS.keys()),
    }

